int	ft_str_is_alpha(char *str)
{
	if (*str == '\0')
		return (1);
	while (*str)
	{
		if (*str >= 65 && *str <= 90)
			str++;
		else if (*str >= 97 && *str <= 122)
			str++;
		else
			return (0);
	}
	return (1);
}
